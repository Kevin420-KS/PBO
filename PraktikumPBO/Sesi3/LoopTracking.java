package PraktikumPBO.Sesi3;

public class LoopTracking {
    public static void main(String[] args) {
        // Menggunakan perulangan for untuk iterasi 1 hingga 5
        for (int i = 1; i <= 5; i++) {
            // Menampilkan iterasi dan angka
            System.out.println("Iterasi ke-" + i + ": Angka " + i);
        }
    }
}
